class Drug {
  final String name;
  final String description;
  final String category;
  final String type;
  final String dosage;
  final String date;
  final String usage;
  final String unusage;
  final String image;

  Drug({
    required this.name,
    required this.description,
    required this.category,
    required this.type,
    required this.dosage,
    required this.date,
    required this.usage,
    required this.unusage,
    required this.image,
  });
}
